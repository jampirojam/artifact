    private void updateuptraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateuptraActionPerformed
        try {
            Session session = ConfigSession.getSessionFactory().openSession();
            session.beginTransaction();
            b.setId(jidUptra.getText());
            b.setNominal(Float.parseFloat(jnomUptra.getText()));
            b.setJenis(jjtraUptra.getSelectedItem().toString());
            b.setId_saldo(idsalUptra.getText());
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd hh:mm:ss");
            LocalDateTime sekarang = LocalDateTime.now();
            Timestamp ts = Timestamp.valueOf(sekarang);
            b.setTanggal(ts);
            session.update(b);
            session.getTransaction().commit();
            JOptionPane.showMessageDialog(this, "Ubah Berhasil");
        } catch (HibernateException he) {
            JOptionPane.showMessageDialog(this, "Ubah Gagal");
            he.printStackTrace();
        }         // TODO add your handling code here:
    }//GEN-LAST:event_updateuptraActionPerformed

