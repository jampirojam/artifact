    public UpdateTransaksi(java.awt.Frame parent, boolean modal, String id) {
        super(parent, modal);
        try {
            Session session = ConfigSession.getSessionFactory().openSession();
            session.beginTransaction();
            Query query = session.createQuery("from Transaksi where id = :id");
            query.setParameter("id", id);
            List ls = query.list();
            Iterator it = ls.iterator();
            while (it.hasNext()) {
                b = (Transaksi) it.next();
            }
            session.getTransaction().commit();
        } catch (HibernateException he) {
            he.printStackTrace();
        }
        initComponents();
        SettingTanggal();

        jidUptra.setText(b.getId());
        jnomUptra.setText(String.valueOf(b.getNominal()));
        jjtraUptra.setSelectedItem(String.valueOf(b.getJenis()));
        idsalUptra.setText(b.getId_saldo());
    }

